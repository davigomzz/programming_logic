# aqui nesse código vou explicar oqeu é são arrays com o elemento de iteração for

# a array é uma estrutura de dados que guarda um conjunto de valores organizados em
# posições consecutivas, no caso aqui existe a sequencia primeiro davi depois jorge e etc
# e quando o elemeno de iteração é chamado pelo for ele se repete nessa ordem já esquematizada pelo array.


nomes = ["Davi", "Jorge", "Maia","kennedy"]

for nome in nomes
  puts "Olá, #{nome}!"
end
